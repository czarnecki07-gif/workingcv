function renderDynamicLists(){
  el('experienceList').innerHTML = state.experience.map((x,i)=>inputCard('experience', i, x)).join('');
  el('educationList').innerHTML = state.education.map((x,i)=>inputCard('education', i, x)).join('');
  el('skillsList').innerHTML = state.skills.map((x,i)=>inputCard('skills', i, x)).join('');
}

function addItem(type){
  if(type==='experience') state.experience.push({position:'',company:'',dates:'',desc:''});
  if(type==='education') state.education.push({school:'',field:'',dates:'',desc:''});
  if(type==='skills') state.skills.push({name:''});
  renderDynamicLists(); renderCv(); save();
}
function updateItem(type, idx, key, value){
  state[type][idx][key] = value;
  renderCv(); save();
}
function removeItem(type, idx){
  state[type].splice(idx,1);
  renderDynamicLists(); renderCv(); save();
}
window.updateItem = updateItem;
window.removeItem = removeItem;

function escapeHtml(s){
  return (s || '').replace(/[&<>"']/g, m => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[m]));
}

