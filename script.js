function save(){
  const data = {
    state,
    inputs: {
      fullName: el('fullName').value, jobTitle: el('jobTitle').value, address: el('address').value, phone: el('phone').value, email: el('email').value, summary: el('summary').value,
      letterName: el('letterName').value, letterAddress: el('letterAddress').value, letterPhone: el('letterPhone').value, letterEmail: el('letterEmail').value, company: el('company').value, position: el('position').value, recipient: el('recipient').value, letterBody: el('letterBody').value
    }
  };
  localStorage.setItem('workingcv_redo', JSON.stringify(data));
}
function load(){
  try{
    const raw = localStorage.getItem('workingcv_redo');
    if(!raw) return;
    const data = JSON.parse(raw);
    Object.assign(state, data.state || {});
    const inputs = data.inputs || {};
    Object.keys(inputs).forEach(id => { if(el(id)) el(id).value = inputs[id]; });
  }catch(e){}
}
function switchMode(mode){
  state.mode = mode;
  document.querySelectorAll('.mode-btn').forEach(btn => btn.classList.toggle('active', btn.dataset.mode===mode));
  el('cvBuilder').classList.toggle('active-section', mode==='cv');
  el('letterBuilder').classList.toggle('active-section', mode==='letter');
  save();
}
function printNode(nodeId){
  switchMode(nodeId === 'cvPreview' ? 'cv' : 'letter');
  setTimeout(() => window.print(), 50);
}
function bind(){
  document.querySelectorAll('.mode-btn').forEach(btn => btn.onclick = () => switchMode(btn.dataset.mode));
  document.querySelectorAll('[data-go]').forEach(btn => btn.onclick = () => switchMode(btn.dataset.go));
  el('langSelect').value = state.lang;
  el('langSelect').onchange = (e) => { state.lang = e.target.value; setTexts(); renderDynamicLists(); save(); };
  ['fullName','jobTitle','address','phone','email','summary','letterName','letterAddress','letterPhone','letterEmail','company','position','recipient','letterBody'].forEach(id=>{
    el(id).addEventListener('input', ()=>{ renderCv(); renderLetter(); save(); });
  });
  el('addExperienceBtn').onclick = ()=>addItem('experience');
  el('addEducationBtn').onclick = ()=>addItem('education');
  el('addSkillBtn').onclick = ()=>addItem('skills');
  el('downloadCvBtn').onclick = ()=>printNode('cvPreview');
  el('downloadLetterBtn').onclick = ()=>printNode('letterPreview');
  el('photoInput').addEventListener('change', (e)=>{
    const file = e.target.files[0];
    if(!file) return;
    const reader = new FileReader();
    reader.onload = ()=>{ state.photo = reader.result; renderCv(); save(); };
    reader.readAsDataURL(file);
  });
}

function init(){
  load();
  renderTemplateCards();
  renderDynamicLists();
  bind();
  setTexts();
  switchMode(state.mode || 'cv');
}
document.addEventListener('DOMContentLoaded', init);
