function renderSentenceBank(){
  const list = sentenceLibrary[state.lang] || sentenceLibrary.en;
  el('sentenceBank').innerHTML = list.map(s => `<button class="sentence-chip" onclick="appendSentence(${JSON.stringify(s)})">${escapeHtml(s)}</button>`).join('');
}
function appendSentence(sentence){
  const ta = el('letterBody');
  ta.value = (ta.value.trim() ? ta.value.trim() + "\n\n" : "") + sentence;
  renderLetter(); save();
}
window.appendSentence = appendSentence;

